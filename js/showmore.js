let mybutton=document.querySelector(".readmorebutton");
let moreinfo=document.querySelector(".moreinfo");
let thedots=document.querySelector(".thedots");

moreinfo.style.display="none";
mybutton.addEventListener("click",dothis);

function dothis(){
	
	if(thedots.style.display==="none"){
		moreinfo.style.display="none";
		mybutton.innerHTML="show more";
		thedots.style.display="inline";

	}
	else{
		moreinfo.style.display="inline";
		mybutton.innerHTML="show less";
		thedots.style.display="none";
	}

}

